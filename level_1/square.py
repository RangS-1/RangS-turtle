import turtle

turtle.backward(200)

for i in range(4):
    turtle.forward(200)
    turtle.right(90)

turtle.forward(200)

for i in range(4):
    turtle.forward(200)
    turtle.right(90)

for i in range(4):
    turtle.forward(200)
    turtle.left(90)

for i in range(4):
    turtle.backward(200)
    turtle.right(90)

turtle.done()
